export function getYearOneLiner(score: number): string {
  const tier = score >= 85 ? 'high' : score >= 70 ? 'medium' : 'low';
  const templates = YEAR_SUMMARY_TEMPLATES[tier];
  return templates[Math.floor(Math.random() * templates.length)];
}

export function getDetailedOneLiner(category: keyof typeof DETAILED_FORTUNE_TEMPLATES, score: number): string {
  const tier = score >= 75 ? 'high' : score >= 50 ? 'medium' : 'low';
  const templates = DETAILED_FORTUNE_TEMPLATES[category][tier];
  return templates[Math.floor(Math.random() * templates.length)];
}

export function getMonthlyOneLiner(score: number): { theme: string; oneLiner: string } {
  const s = Math.min(5, Math.max(1, score)) as keyof typeof MONTHLY_TEMPLATES;
  return MONTHLY_TEMPLATES[s];
}
